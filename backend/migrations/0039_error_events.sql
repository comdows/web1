-- ============================================================
-- 세모플 0039 — 프론트 오류 수집: event_t에 'error' 추가
-- (0001~0038 실행된 DB에 이어 실행 · 멱등)
--
-- 배경(관측성): 실사용자 브라우저의 미처리 예외·흰 화면은 서버 로그에 안 남는다.
--   클라이언트가 events(type='error', query=메시지, entity_type=뷰명)로 기록하고
--   관리 콘솔이 열람(읽기는 기존 admin-only RLS 그대로). 쓰기도 기존
--   "anyone insert event" 정책 그대로 — 신규 테이블·정책 없음.
--
-- 설계 노트:
--   · 성장 지표 오염 방지 — 클라이언트는 오류 이벤트에 session_id·user_id를 싣지 않는다
--     (주간 세션·WAU·리텐션 집계가 session/user 기준이라 null이면 자동 제외).
--   · 90일 purge(0010)가 오류 행도 함께 정리 — 별도 보존 정책 불필요.
--   · 이 파일은 ALTER TYPE만 단독 수행 — 새 enum 값을 같은 트랜잭션에서 사용하면
--     PG가 거부하므로(unsafe use of new value) 인덱스 등 사용처는 필요 시 후속 번호로.
-- ============================================================

alter type public.event_t add value if not exists 'error';
